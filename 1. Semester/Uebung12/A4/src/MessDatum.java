
public class MessDatum {

    private final int u;
    private final double t;
    private final double d;

    public MessDatum(int u, double t, double d) {
        this.u = u;
        this.t = t;
        this.d = d;
    }

    public int getU() {
        return u;
    }

    public double getT() {
        return t;
    }

    public double getD() {
        return d;
    }
}
